# 자료형

# 1) 숫자 자료형
print(5)

# 2) 문자열 자료형
print('안녕하세요')
print("풍선")
print("ㅋ"*9)
# 문자열에 수식을 이용하여 문장을 간결하게 표현 가능하다.
print("철수는 영희에게 '나는 너가 좋아'라고 말했다.")
# 문자열 자료형에서 따음표를 쓰기 위해서 큰따음표와 작은 따음표를 함께 사용할수 있다. 

# 3) boolean 자료형 (참/거짓)
print(5 > 10)
print(True)
print(False)
# not 키워드는 결과 값을 반전 시켜준다
print(not True)